# Dictionary Methods Example

# Original dictionary
student = {"name": "Alice", "age": 20, "grade": "A"}

# Method 1: get
student_name = student.get("name")
print("Using get():", student_name)

# Method 2: keys
student_keys = student.keys()
print("Using keys():", student_keys)

# Method 3: values
student_values = student.values()
print("Using values():", student_values)
