# Question 7: Compute the square of a number using a lambda function

# Lambda function to compute the square of a number
square = lambda x: x ** 2

# Compute the square of 5 and 12
square_of_5 = square(5)
square_of_12 = square(12)

# Display results
print("Square of 5:", square_of_5)
print("Square of 12:", square_of_12)
